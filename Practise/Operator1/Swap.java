package Practise.Operator1;
//Swap two numbers using only arithmetic operators (without temp variable).

public class Swap {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        System.out.println("Before swap: a = " + a + ", b = " + b);

        // Swap using arithmetic operators
        a = a + b;
        b = a - b;
        a = a - b;

        System.out.println("After swap: a = " + a + ", b = " + b);
    }
}

